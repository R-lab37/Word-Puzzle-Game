  String get scrambledWord => _scrambledWord;

  void startGame() {
    _correctWord = _getRandomWord();
    _scrambledWord = _scrambleWord(_correctWord);
  }

  String _getRandomWord() {
    final random = Random();
    return _wordBank[random.nextInt(_wordBank.length)];
  }

  String _scrambleWord(String word) {
    final List<String> letters = word.split('');
    letters.shuffle();
    return letters.join();
  }

  bool checkAnswer(String answer) {
    return answer.toUpperCase() == _correctWord;
  }
